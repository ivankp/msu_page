function updateTable() {
  var table = document.getElementById("plots_table");

  for (var r=0; r<table.rows.length; ++r) {
    var row = table.rows[r];
    if ( table.rows[r].getAttribute("class") == "plots" ) {
      table.removeChild( table.rows[r--] );
    }
  }

  for (var i=0; i<data.length; i++) {
    var ok = true;
    for (var j=0; j<fields.length; j++) {
      if (selected[j] != "Any") {
        if (data[i][j] != selected[j]) {
          ok = false;
          continue;
        }
      }
    }

    // insert content
    if (ok == true) {
      var row = document.createElement('tr');
      row.setAttribute('class',"plots");
      for (var k=0; k<fields.length; ++k) {
        var cell = document.createElement('td');
        cell.innerHTML = data[i][k];
        row.appendChild(cell);
      }

      var link = data[i][0] + data[i][1] + "j_"
               + data[i][2] +'_'+ data[i][9]+'_'
               + data[i][3] +'_'+ data[i][4]+'_'
               + data[i][5];

      if (data[i][6]!="none") link += "_VBF" + data[i][6];
      if (data[i][7]!="none") link += "_MAA" + data[i][7];
      if (data[i][8]!="4.4")  link += "_eta" + data[i][8];

      var cell = document.createElement('td');
      var a = document.createElement('a');
      a.setAttribute("href","hist/"+link+".pdf");
      a.setAttribute("target","_blank");
      a.innerHTML = "plots";
      cell.appendChild(a);
      row.appendChild(cell);

      cell = document.createElement('td');
      a = document.createElement('a');
      a.setAttribute("href","hist/"+link+".root");
      a.setAttribute("download",'');
      a.innerHTML = "root";
      cell.appendChild(a);
      row.appendChild(cell);

      cell = document.createElement('td');
      a = document.createElement('a');
      a.setAttribute("href","hist/"+link+".yoda.tar.bz2");
      a.setAttribute("download",'');
      a.innerHTML = "yoda";
      cell.appendChild(a);
      row.appendChild(cell);

      table.appendChild(row);
    }
  }
}

function makeChange(i) {
  selected[i] = document.getElementById(fields[i]).value;
  updateTable();
}

function changeTo(i,val) {
  document.getElementById(fields[i]).value = val;
  makeChange(i);
}

window.onload = function() {
  for (var j=0; j<fields.length; j++) values[j] = ["Any"];

  for (var i=0; i<data.length; i++) {
    for (var j=0; j<fields.length; j++) {

      var found = false;

      var k;
      for (var k=1; k<values[j].length; ++k) {
        if ( values[j][k] == data[i][j] ) found = true;
      }

      if (!found) values[j][k] = data[i][j];
    }
  }

  for (var i=0; i<fields.length; i++) {
    var select = document.getElementById(fields[i]);

    for (var j=0; j<values[i].length; j++) {
      var opt = document.createElement('option');
      opt.setAttribute('value',values[i][j]);
      opt.innerHTML = values[i][j];
      select.appendChild(opt);
    }

    selected[i] = document.getElementById(fields[i]).value;

    select.setAttribute('onchange',"makeChange("+i+")");
  }

  changeTo(3,"HT2-unc");
  changeTo(4,"AntiKt4");
  changeTo(5,"CT10nlo");
  changeTo(6,"none");
  changeTo(7,"none");
  changeTo(8,"4.4");
  changeTo(9,"NLO");

  updateTable();
};
